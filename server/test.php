function createDbConnection()
{
    global $servername, $username, $password, $dbname;

//    $servername = "mysql.falazar.com";
//    $username = "falazar";
//    $password = "Buggeroffnow42";
//    $dbname = "sixheroes";


    print "Prepping database.";

    // Create connection
    $conn = new mysqli($servername, $username, $password, $dbname);

    // Check connection
    if ($conn->connect_error) {
        die("Connection failed=> " . $conn->connect_error);
    }

    return $conn;
}

function insertGridData($grid)
{
    $conn = createDbConnection();

    // Iterate over the grid array
    foreach ($grid as $x => $row) {
        foreach ($row as $y => $cell) {
            $terrain = $cell['terrain'];
            $resourceType = array_key_exists('resourceType', $cell) ? $cell['resourceType'] : '';
            $resourceId = array_key_exists('resourceId', $cell) ? $cell['resourceId'] : 0;
            $resourceAmount = array_key_exists('resourceAmount', $cell) ? $cell['resourceAmount'] : 0;
            $armyId = array_key_exists('armyId', $cell) ? $cell['armyId'] : 0;
            $ownerId = array_key_exists('ownerId', $cell) ? $cell['ownerId'] : 0;

            $sql = "INSERT INTO Grid (x, y, terrain, resourceType, resourceId, resourceAmount, armyId, ownerId)
            VALUES ($x, $y, $terrain, '$resourceType', $resourceId, $resourceAmount, $armyId, $ownerId)";
            print "sql = $sql<br>";

            if ($conn->query($sql) !== TRUE) {
                echo "Error=> " . $sql . "<br>" . $conn->error;
            }
        }
    }

    $conn->close();
}


